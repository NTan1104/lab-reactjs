// Data 1
const dolphin_score = (96 + 108 + 89) / 3;
const koala_score = (88 + 91 + 110) / 3;
console.log(`Dolphin's average score: ${dolphin_score}`);
console.log(`Koala's average score: ${koala_score}`);
if (dolphin_score > koala_score && dolphin_score >= 100) {
    console.log('Dolphin team wins the trophy!');
} else if (koala_score > dolphin_score && koala_score >= 100) {
    console.log('Koala team wins the trophy!');
} else if (dolphin_score === koala_score && dolphin_score >= 100 && koala_score >= 100) {
    console.log("It's a draw!");
} else {
    console.log('No team wins the trophy.');
}

